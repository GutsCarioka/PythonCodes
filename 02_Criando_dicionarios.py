# Dicionário: Classificação de objetos não repitidos que funcionam em pares que são chaves e valores 
pessoa = {"nome": "Guilherme", "idade": 28}
print(pessoa)

pessoa = dict(nome = "Guilherme", idade = 28 )
print(pessoa)

pessoa["telefone"] = "3333-1234"
print(pessoa)

pessoa = {"a": 1234}
print(pessoa)
pessoa = dict()
print(pessoa)